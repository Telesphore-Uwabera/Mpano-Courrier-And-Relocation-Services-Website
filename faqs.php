      const updateCount = () => {
        const target = +counter.getAttribute('data-target'); // Get target number
        const count = +counter.innerText; // Current value
        const increment = target / 100; // Adjust speed
  
        if (count < target) {
          counter.innerText = Math.ceil(count + increment);
